package com.RepoMarcoGithub;

public class Main {
    public static void main(String[] args) {

        //1 numericos

        //1.1 enteros
        byte variable1 = 40;
        System.out.println(variable1);

        short variable2 = 5000;
        System.out.println(variable2);

        int variable3 = 1000000000;
        System.out.println(variable3);

        long variable4 = 1000000000;
        System.out.println(variable4);

        //1.2 decimales
        float variable5 = 5.5f;
        System.out.println(variable5);

        double variable6 = 15.5000000000000d;
        System.out.println(variable6);

        //booleanos
        boolean variable7 = true;
        System.out.println(variable7);

        boolean variable8 = false;
        System.out.println(variable8);

        //texto
        char variable9 = 'a';
        System.out.println(variable9);

        String variable10 = "hola mundo del mundo mundial";
        System.out.println(variable10);
    }
}
